def apply_common_arg_names(args: list[str], func_name: str | None, include_self: bool) -> list[str]:
    if not func_name:
        return args
    names = COMMON_SIGNATURE_NAMES.get(func_name)
    if not names:
        return args
    result = list(args)
    start = 1 if include_self and result and result[0] == "self" else 0
    for offset, name in enumerate(names):
        index = start + offset
        if index >= len(result):
            break
        part = result[index]
        if part.startswith("*args") or part.startswith("**kwargs") or ":" not in part:
            continue
        current, rest = part.split(":", 1)
        if re.fullmatch(r"arg\d*", current.strip()):
            result[index] = f"{name}:{rest}"
    return result


def add_kw_hints(args: list[str], kw_hints: list[str]) -> list[str]:
    if not kw_hints:
        return args
    result = [arg for arg in args if arg != "**kwargs: Any"]
    used_names = {part.split(":", 1)[0].strip() for part in result if ":" in part}
    filtered = [hint for hint in kw_hints if hint.split(":", 1)[0].strip() not in used_names]
    if not filtered:
        return result
    if "*" not in result:
        result.append("*")
    result.extend(filtered)
    return result


def function_signature(func: CFunction, *, include_self: bool, name: str | None = None) -> str:
    func_name = name or func.obj
    if func.allowed_args:
        args = (["self"] if include_self else []) + func.allowed_args
        args = add_kw_hints(args, func.kw_hints)
        args = apply_common_arg_names(args, func_name, include_self)
        return signature_from_args(args, infer_return_type(func_name))
    if func.min_args is not None:
        min_args = max(func.min_args, func.min_args_hint or 0)
        args = args_from_arity(min_args, func.max_args, include_self=include_self, accepts_kw=func.accepts_kw, positional_only=func.positional_only, arg_hints=func.arg_hints)
        args = add_kw_hints(args, func.kw_hints)
        args = apply_common_arg_names(args, func_name, include_self)
        return signature_from_args(args, infer_return_type(func_name))
    return "(self, *args: Any, **kwargs: Any) -> Any" if include_self else "(*args: Any, **kwargs: Any) -> Any"


STREAM_METHOD_SIGNATURES = {
    "mp_stream_read": "(self, nbytes: Optional[int] = None) -> bytes",
    "mp_stream_readinto": "(self, buf: Any, nbytes: Optional[int] = None) -> int",
    "mp_stream_unbuffered_readline": "(self) -> bytes",
    "mp_stream_write": "(self, buf: Any) -> int",
}


def stream_method_signature(obj: str) -> str | None:
    return STREAM_METHOD_SIGNATURES.get(obj)


def function_uses_kw_args(text: str, func_name: str) -> bool:
    snippet = function_body_snippet(text, func_name)
    if not snippet:
        return False
    open_brace = snippet.find("{")
    body = snippet[open_brace + 1:] if open_brace >= 0 else snippet
    body = re.sub(r"\(\s*void\s*\)\s*kw_args\s*;", "", body)
    return "kw_args" in body


def collect_functions(text: str) -> dict[str, CFunction]:
    functions: dict[str, CFunction] = {}
    for match in FUN_OBJ_FIXED_RE.finditer(text):
        argc = int(match.group("argc"))
        obj = match.group("obj")
        functions[obj] = CFunction(obj=obj, func=match.group("func"), min_args=argc, max_args=argc)
    for match in FUN_OBJ_VAR_RE.finditer(text):
        obj = match.group("obj")
        max_group = match.group("max")
        functions[obj] = CFunction(
            obj=obj,
            func=match.group("func"),
            min_args=int(match.group("min")),
            max_args=int(max_group) if max_group is not None else None,
        )
    for match in FUN_OBJ_KW_RE.finditer(text):
        obj = match.group("obj")
        functions[obj] = CFunction(obj=obj, func=match.group("func"), min_args=int(match.group("min")), max_args=None, accepts_kw=True)

    for match in STATICMETHOD_OBJ_RE.finditer(text):
        alias = match.group("alias")
        target = match.group("target")
        target_obj = target[:-4] if target.endswith("_obj") else target
        base = functions.get(target_obj)
        if base is not None:
            functions[alias] = CFunction(
                obj=alias,
                func=base.func,
                min_args=base.min_args,
                max_args=base.max_args,
                accepts_kw=base.accepts_kw,
                positional_only=base.positional_only,
                is_staticmethod=True,
            )

    for fn in functions.values():
        fn.allowed_args = parse_allowed_args(text, fn.func)
        if not fn.allowed_args:
            delegated = delegated_allowed_args(text, fn.func)
            if delegated:
                fn.allowed_args = delegated[1]
        fn.arg_hints = infer_arg_hints(text, fn.func)
        fn.min_args_hint = infer_min_args_hint(text, fn.func)
        fn.kw_hints = infer_kw_hints(text, fn.func)
        checked = parse_arg_check_num(text, fn.func)
        if checked and not fn.allowed_args:
            fn.min_args, fn.max_args, fn.accepts_kw = checked
        if not fn.allowed_args and fn.accepts_kw and not function_uses_kw_args(text, fn.func):
            fn.accepts_kw = False
            fn.positional_only = True
        elif not fn.accepts_kw:
            fn.positional_only = True
    return functions


def locals_dict_from_body(body: str) -> str | None:
    match = re.search(r"locals_dict\s*,\s*&(?P<dict>[A-Za-z0-9_]+)", body)
    return match.group("dict") if match else None


def make_new_from_body(body: str) -> str | None:
    match = re.search(r"make_new\s*,\s*(?P<func>[A-Za-z0-9_]+)", body)
    return match.group("func") if match else None


def globals_dict_from_module_body(body: str) -> str | None:
    match = re.search(r"\.globals\s*=\s*\([^)]*\)\s*&(?P<dict>[A-Za-z0-9_]+)", body)
    if not match:
        match = re.search(r"globals\s*=\s*\([^)]*\)\s*&(?P<dict>[A-Za-z0-9_]+)", body)
    return match.group("dict") if match else None


def analyze_c_sources(sources: Iterable[Path]) -> CAnalysis:
    analysis = CAnalysis(sources=sorted(set(sources)))
    source_text: dict[Path, str] = {}
    c_macros: dict[str, str] = {}

    for src in analysis.sources:
        text = src.read_text(errors="ignore")
        source_text[src] = text
        c_macros.update(collect_c_macros(text))
        # Also collect macros from locally-included headers so that table-body
        # macros like OPENCV_CORE_GLOBALS can be expanded.
        c_macros.update(resolve_local_includes(src))
        for match in ROM_MAP_RE.finditer(text):
            body = expand_table_body_macros(match.group("body"), c_macros)
            analysis.table_bodies[match.group("name")] = body
        for match in CONST_DICT_RE.finditer(text):
            analysis.dict_tables[match.group("dict")] = match.group("table")
        analysis.functions.update(collect_functions(text))

    for src, text in source_text.items():
        for match in TYPE_RE.finditer(text):
            type_var = match.group("type")
            body = match.group("body")
            ctype = CType(
                type_var=type_var,
                qstr=match.group("qstr"),
                make_new=make_new_from_body(body),
                locals_dict=locals_dict_from_body(body),
            )
            if ctype.make_new:
                ctype.init_args = infer_init_args(text, ctype.make_new)
            analysis.types[type_var] = ctype

        for match in MODULE_DEF_RE.finditer(text):
            module_var = match.group("var")
            globals_dict = globals_dict_from_module_body(match.group("body"))
            for register in REGISTER_RE.finditer(text):
                if register.group("var") == module_var:
                    module_name = qstr_module_name(register.group("module"))
                    analysis.modules[module_name] = CModule(
                        name=module_name,
                        var=module_var,
                        globals_dict=globals_dict,
                        table=analysis.dict_tables.get(globals_dict or ""),
                        source=str(src),
                    )

    network_interfaces = expand_c_macro("MICROPY_PORT_NETWORK_INTERFACES", c_macros)
    if network_interfaces:
        analysis.module_extra_bodies["network"] = network_interfaces

    for ctype in analysis.types.values():
        if not ctype.locals_dict:
            continue
        table = analysis.dict_tables.get(ctype.locals_dict)
        body = analysis.table_bodies.get(table or "", "")
        for entry in ENTRY_RE.finditer(body):
            name = entry.group("name")
            if name == "__del__" or not valid_stub_identifier(name):
                continue
            rhs = entry.group("rhs")
            ptr = PTR_RE.search(rhs)
            if ptr:
                var = ptr.group("var")
                obj = var[:-4] if var.endswith("_obj") else var
                if obj == "py_func_unavailable" and name in ctype.methods:
                    continue
                func = analysis.functions.get(obj)
                if func is None:
                    if "MP_ROM_INT" in rhs or name.isupper():
                        ctype.attrs[name] = "int"
                    continue
                signature = stream_method_signature(obj)
                if signature is None:
                    signature = function_signature(func, include_self=not func.is_staticmethod, name=name)
                current = ctype.methods.get(name)
                generic_self = "(self, *args: Any, **kwargs: Any) -> Any"
                generic_static = "(*args: Any, **kwargs: Any) -> Any"
                if current is None or current in {generic_self, generic_static} or obj != "py_func_unavailable":
                    ctype.methods[name] = signature
            elif "MP_ROM_INT" in rhs or name.isupper():
                ctype.attrs[name] = "int"

        # Synthesize dunder methods from MicroPython type slots.
        _apply_type_slot_methods(ctype, analysis)

    return analysis


