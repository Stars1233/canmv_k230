"""Generate CanMV K230 MicroPython stubs for IDE completion.

The generator is source/build based:
- frozen_mpy/ is used to inventory frozen Python modules for the selected board.
- genhdr/qstr/*.qstr is used to identify the C/C++ sources compiled for that board.
- C module globals, object types, locals dictionaries and mp_arg_t tables are parsed
  to discover names and first-pass signatures.
- tools/canmv_stub_overrides.json is only for polishing signatures/docs when C
  source does not preserve enough type or parameter information.

For standard MicroPython/library modules, bundle the matching micropython-stubs
package beside this CanMV overlay.
"""

