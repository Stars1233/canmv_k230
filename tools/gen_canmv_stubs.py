def collect_c_macros(text: str) -> dict[str, str]:
    macros: dict[str, str] = {}
    lines = text.splitlines()
    index = 0
    while index < len(lines):
        line = lines[index]
        stripped = line.lstrip()
        if not stripped.startswith("#define "):
            index += 1
            continue

        parts = stripped[len("#define "):].split(None, 1)
        if not parts:
            index += 1
            continue
        name = parts[0]
        body_parts = [parts[1] if len(parts) > 1 else ""]
        while body_parts[-1].rstrip().endswith("\\") and index + 1 < len(lines):
            body_parts[-1] = body_parts[-1].rstrip()[:-1]
            index += 1
            body_parts.append(lines[index])
        body = "\n".join(body_parts)
        # Prefer the first non-empty definition when multiple #define's
        # exist for the same name (e.g. inside #if / #else / #endif).
        if name not in macros or (not macros[name].strip() and body.strip()):
            macros[name] = body
        index += 1
    return macros


def expand_c_macro(name: str, macros: dict[str, str], seen: set[str] | None = None) -> str:
    if seen is None:
        seen = set()
    if name in seen:
        return ""
    body = macros.get(name, "")
    seen.add(name)
    for token in sorted(set(re.findall(r"\b[A-Z][A-Z0-9_]+\b", body))):
        if token in macros:
            body = re.sub(r"\b" + re.escape(token) + r"\b", expand_c_macro(token, macros, seen.copy()), body)
    return body


_INCLUDE_RE = re.compile(r'^\s*#\s*include\s+"(?P<path>[^"]+)"', re.MULTILINE)
_INCLUDE_SEARCH_DIRS: list[Path] | None = None


def _include_search_dirs() -> list[Path]:
    """Return project directories to search when resolving ``#include "..."``."""
    global _INCLUDE_SEARCH_DIRS
    if _INCLUDE_SEARCH_DIRS is None:
        dirs: list[Path] = []
        for root in (PORT_DIR, MICROPY_DIR, CANMV_DIR):
            if root.exists():
                dirs.append(root)
        _INCLUDE_SEARCH_DIRS = dirs
    return _INCLUDE_SEARCH_DIRS


def resolve_local_includes(source_path: Path) -> dict[str, str]:
    """Read source file, find ``#include "..."`` directives, resolve them against
    the project tree, and return a merged dict of all macros from those headers.
    """
    try:
        text = source_path.read_text(errors="ignore")
    except OSError:
        return {}
    macros: dict[str, str] = {}
    source_dir = source_path.parent
    search_dirs = [source_dir] + _include_search_dirs()
    seen: set[Path] = set()
    for match in _INCLUDE_RE.finditer(text):
        include_path = match.group("path")
        for base in search_dirs:
            candidate = (base / include_path).resolve()
            if candidate.exists() and candidate not in seen:
                seen.add(candidate)
                try:
                    header_text = candidate.read_text(errors="ignore")
                except OSError:
                    continue
                macros.update(collect_c_macros(header_text))
                break
    return macros


def expand_table_body_macros(body: str, macros: dict[str, str]) -> str:
    """Expand C preprocessor macros found inside a ROM table body.

    Only all-uppercase identifiers that reference known macros are expanded;
    everything else is left unchanged.
    """
    if not macros:
        return body
    # Find standalone uppercase identifiers that could be macros
    candidates = set(re.findall(r"\b([A-Z][A-Z0-9_]{2,})\b", body))
    result = body
    for name in sorted(candidates, key=len, reverse=True):
        if name in macros:
            expanded = expand_c_macro(name, macros)
            if expanded and expanded != name:
                result = result.replace(name, expanded)
    return result


def module_name_from_mpy(path: Path, root: Path) -> str:
    rel = path.relative_to(root).with_suffix("")
    parts = list(rel.parts)
    if parts and parts[-1] == "__init__":
        parts.pop()
    return ".".join(parts)


def discover_frozen_modules(build_dir: Path | None) -> list[str]:
    if build_dir is None:
        return []
    frozen_mpy = build_dir / "frozen_mpy"
    if not frozen_mpy.exists():
        return []
    return sorted({module_name_from_mpy(path, frozen_mpy) for path in frozen_mpy.rglob("*.mpy")})


def qstr_stem_to_source(qstr_file: Path) -> Path | None:
    """Map a .qstr filename back to its original C/C++ source file.

    Three naming conventions are handled:

    1. ``@@__micropython__extmod__modtime.c.qstr``
       Files from the MicroPython core (``MICROPY_DIR``).  Leading ``@@`` marks
       the canonical upstream tree; the remainder is a relative path with ``__``
       separators.

    2. ``drivers__sensor__sc2236.c.qstr``
       First-party port sources under ``PORT_DIR``.  No prefix ― the stem is a
       relative path from ``PORT_DIR`` with ``__`` separators.

    3. ``__home__...__output__...__lvgl__lv_mpy.c.qstr``
       Generated or out-of-tree sources whose QSTR stem is an absolute path
       encoded with ``__`` as directory separators.  These files live inside
       the build directory itself.
    """
    stem = remove_suffix(qstr_file.name, ".qstr")
    if stem.startswith("@@__micropython__"):
        rel = remove_prefix(stem, "@@__micropython__").replace("__", "/")
        src = MICROPY_DIR / rel
    elif stem.startswith("__"):
        # Absolute-path encoding: /home/.../output/.../lvgl/lv_mpy.c
        # becomes __home__...__output__...__lvgl__lv_mpy.c
        candidate = Path("/" + stem.replace("__", "/"))
        if candidate.exists():
            return candidate
        return None
    else:
        src = PORT_DIR / stem.replace("__", "/")
    return src if src.exists() else None


def discover_qstr_sources(build_dir: Path | None) -> list[Path]:
    if build_dir is None:
        return []
    qstr_dir = build_dir / "genhdr" / "qstr"
    if not qstr_dir.exists():
        return []
    sources = []
    for qstr_file in qstr_dir.glob("*.qstr"):
        src = qstr_stem_to_source(qstr_file)
        if src is not None:
            sources.append(src)
    return sorted(set(sources))


def discover_sibling_sources(qstr_sources: list[Path]) -> list[Path]:
    """Find C/C++ files that live in the same directory subtrees as *qstr_sources*
    but don't have their own .qstr file.  These are typically helper translation
    units (e.g. ``bitwise.c``, ``compare.c`` in ulab) whose function objects are
    referenced from another file's ROM table.
    """
    known = set(qstr_sources)
    result: list[Path] = []
    seen_dirs: set[Path] = set()
    for src in qstr_sources:
        parent = src.parent
        if parent in seen_dirs:
            continue
        seen_dirs.add(parent)
        # Walk up to 4 levels deep from the source directory
        for depth in range(5):
            search_root = parent
            for _ in range(depth):
                search_root = search_root.parent
            if not _is_within_project(search_root):
                continue
            for c_file in sorted(search_root.glob("*.c")):
                if c_file not in known and c_file not in result:
                    result.append(c_file)
            for cpp_file in sorted(search_root.glob("*.cpp")):
                if cpp_file not in known and cpp_file not in result:
                    result.append(cpp_file)
    return sorted(set(result))


def _is_within_project(path: Path) -> bool:
    """Return True if *path* is inside PORT_DIR or MICROPY_DIR."""
    try:
        path.resolve().relative_to(PORT_DIR.resolve())
        return True
    except ValueError:
        pass
    try:
        path.resolve().relative_to(MICROPY_DIR.resolve())
        return True
    except ValueError:
        pass
    return False


def fallback_sources() -> list[Path]:
    patterns = ("*.c", "*.cpp")
    sources: list[Path] = []
    for root in (PORT_DIR, MICROPY_DIR / "py", MICROPY_DIR / "extmod"):
        if root.exists():
            for pattern in patterns:
                sources.extend(root.rglob(pattern))
    return sorted(set(sources))


def load_overrides(path: Path | None) -> OverrideMap:
    if path is None or not path.exists():
        return {}
    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)
    return {k: v for k, v in data.items() if not k.startswith("__")}


def module_override(overrides: OverrideMap, module: str) -> dict[str, Any]:
    override = overrides.get(module, {})
    return override if isinstance(override, dict) else {}


def module_symbols(overrides: OverrideMap, module: str) -> dict[str, Any]:
    symbols = module_override(overrides, module).get("symbols", {})
    return symbols if isinstance(symbols, dict) else {}


def deep_merge_dict(left: dict[str, Any], right: dict[str, Any]) -> dict[str, Any]:
    merged = dict(left)
    for key, value in right.items():
        if isinstance(value, dict) and isinstance(merged.get(key), dict):
            merged[key] = deep_merge_dict(merged[key], value)
        else:
            merged[key] = value
    return merged


def merge_overrides(*maps: OverrideMap) -> OverrideMap:
    result: OverrideMap = {}
    for override_map in maps:
        for module, override in override_map.items():
            if not isinstance(override, dict):
                continue
            result[module] = deep_merge_dict(result.get(module, {}), override)
    return result


def signature_from_stub_node(lines: list[str], node: ast.FunctionDef | ast.AsyncFunctionDef) -> str:
    line = lines[node.lineno - 1]
    match = re.match(r"\s*(?:async\s+def|def)\s+" + re.escape(node.name) + r"\s*(?P<sig>\(.*\)\s*(?:->\s*[^:]+)?)\s*:", line)
    return ensure_signature(match.group("sig"), format_signature(node)) if match else format_signature(node)


def annotation_from_stub_assignment(lines: list[str], node: ast.Assign | ast.AnnAssign) -> str:
    if isinstance(node, ast.AnnAssign):
        line = lines[node.lineno - 1]
        match = re.match(r"\s*[A-Za-z_][A-Za-z0-9_]*\s*:\s*(?P<typ>[^=#]+)", line)
        if match:
            return match.group("typ").strip()
    return "Any"


def stub_doc_block_to_override(default_module: str | None, block_lines: list[str]) -> OverrideMap:
    module = default_module
    body_lines: list[str] = []
    for line in block_lines:
        match = re.match(r"\s*module\s*:\s*(?P<module>[A-Za-z_][A-Za-z0-9_.]*)\s*$", line)
        if match:
            module = match.group("module")
        else:
            body_lines.append(line)
    if not module or not any(line.strip() for line in body_lines):
        return {}

    text = "\n".join(body_lines)
    try:
        tree = ast.parse(text)
    except SyntaxError:
        return {}

    lines = text.splitlines()
    module_data: dict[str, Any] = {}
    module_doc = ast.get_docstring(tree)
    if module_doc:
        module_data["doc"] = module_doc
    symbols: dict[str, Any] = {}
    for node in tree.body:
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and public_name(node.name):
            symbols[node.name] = {
                "kind": "function",
                "signature": signature_from_stub_node(lines, node),
            }
            doc = ast.get_docstring(node)
            if doc:
                symbols[node.name]["doc"] = doc
        elif isinstance(node, ast.ClassDef) and public_name(node.name):
            class_override: dict[str, Any] = {"kind": "class"}
            doc = ast.get_docstring(node)
            if doc:
                class_override["doc"] = doc
            methods: dict[str, Any] = {}
            attrs: dict[str, str] = {}
            for item in node.body:
                if isinstance(item, (ast.FunctionDef, ast.AsyncFunctionDef)) and public_name(item.name):
                    method = {"signature": signature_from_stub_node(lines, item)}
                    method_doc = ast.get_docstring(item)
                    if method_doc:
                        method["doc"] = method_doc
                    if item.name == "__init__":
                        class_override["init"] = method["signature"]
                        if method_doc:
                            class_override["init_doc"] = method_doc
                    else:
                        methods[item.name] = method
                elif isinstance(item, (ast.Assign, ast.AnnAssign)):
                    name = is_public_assignment(item)
                    if name:
                        attrs[name] = annotation_from_stub_assignment(lines, item)
            if methods:
                class_override["methods"] = methods
            if attrs:
                class_override["attrs"] = attrs
            symbols[node.name] = class_override
        elif isinstance(node, (ast.Assign, ast.AnnAssign)):
            name = is_public_assignment(node)
            if name:
                symbols[name] = {"kind": "const", "type": annotation_from_stub_assignment(lines, node)}
    if symbols:
        module_data["symbols"] = symbols
    return {module: module_data} if module_data else {}


def port_doc_overrides_from_file(path: Path, default_module: str | None = None) -> OverrideMap:
    try:
        path.resolve().relative_to(PORT_DIR.resolve())
    except ValueError:
        return {}

    overrides: OverrideMap = {}
    block: list[str] = []
    for line in path.read_text(errors="ignore").splitlines():
        match = STUB_DOC_LINE_RE.match(line)
        if match:
            block.append(match.group("text"))
            continue
        if block:
            overrides = merge_overrides(overrides, stub_doc_block_to_override(default_module, block))
            block = []
    if block:
        overrides = merge_overrides(overrides, stub_doc_block_to_override(default_module, block))
    return overrides


def collect_port_doc_overrides(analysis: CAnalysis, manifest_sources: list[PythonStubSource]) -> OverrideMap:
    source_default_modules: dict[Path, str | None] = {}
    module_sources: dict[Path, list[str]] = {}
    for module in analysis.modules.values():
        if module.source:
            module_sources.setdefault(Path(module.source).resolve(), []).append(module.name)
    for path, modules in module_sources.items():
        source_default_modules[path] = modules[0] if len(modules) == 1 else None
    for source in manifest_sources:
        source_default_modules[source.src.resolve()] = source.module
    for source in analysis.sources:
        source_default_modules.setdefault(source.resolve(), None)

    overrides: OverrideMap = {}
    for path, default_module in sorted(source_default_modules.items()):
        if path.exists():
            overrides = merge_overrides(overrides, port_doc_overrides_from_file(path, default_module))
    return overrides


def doc_lines(doc: str | None, indent: str) -> list[str]:
    return [indent + json.dumps(doc)] if doc else []


def class_signature_is_static(signature: str) -> bool:
    match = re.match(r"\((?P<args>.*?)\)\s*->", signature.strip())
    if not match:
        return False
    args_text = match.group("args").strip()
    if not args_text:
        return True
    for part in args_text.split(","):
        name = part.strip().split(":", 1)[0].split("=", 1)[0].strip()
        if name in {"*", "/"}:
            continue
        if name.startswith("*"):
            return True
        return name not in {"self", "cls"}
    return True


def class_method_decorators(signature: str, explicit: list[str] | None = None) -> list[str] | None:
    decorators = list(explicit or [])
    if not decorators and class_signature_is_static(signature):
        decorators.append("@staticmethod")
    return decorators or None


def ensure_signature(signature: str | None, fallback: str) -> str:
    if not signature:
        return fallback
    signature = signature.strip()
    return signature if signature.startswith("(") else "(" + signature


def render_function(
    name: str,
    signature: str,
    doc: str | None = None,
    indent: str = "",
    prefix: str = "def",
    decorators: list[str] | None = None,
) -> list[str]:
    lines = [f"{indent}{decorator}" for decorator in decorators or []]
    lines.append(f"{indent}{prefix} {name}{signature}:")
    lines.extend(doc_lines(doc, indent + "    "))
    lines.append(f"{indent}    ...")
    return lines


