def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--out",
        default=os.environ.get("CANMV_STUB_OUT", str(CANMV_DIR / "generated" / "ide-stubs" / "canmv-k230")),
        help="output directory for generated stubs",
    )
    parser.add_argument(
        "--overrides",
        default=str(DEFAULT_OVERRIDES),
        help="JSON file with hand-written signatures and docs",
    )
    parser.add_argument(
        "--build-dir",
        default=os.environ.get("CANMV_BUILD_DIR"),
        help="optional built CanMV directory containing frozen_mpy/ and genhdr/qstr/",
    )
    parser.add_argument(
        "--manifest",
        default=os.environ.get("CANMV_MANIFEST"),
        help="board manifest.py to resolve frozen Python sources",
    )
    parser.add_argument(
        "--no-auto-build-dir",
        action="store_true",
        help="do not auto-detect output/*/canmv when --build-dir is omitted",
    )
    args = parser.parse_args(argv)

    out_dir = Path(args.out).resolve()
    override_path = Path(args.overrides).resolve() if args.overrides else None
    file_overrides = load_overrides(override_path)
    build_dir = Path(args.build_dir).resolve() if args.build_dir else None
    if build_dir is None and not args.no_auto_build_dir:
        build_dir = default_build_dir()

    manifest_path = Path(args.manifest).resolve() if args.manifest else manifest_path_from_build(build_dir)
    frozen_modules = discover_frozen_modules(build_dir)
    manifest_sources = discover_manifest_python_sources(manifest_path)
    qstr_sources = discover_qstr_sources(build_dir)
    if qstr_sources:
        qstr_sources = sorted(set(qstr_sources + discover_sibling_sources(qstr_sources)))
    analysis = analyze_c_sources(qstr_sources or fallback_sources())
    source_overrides = collect_port_doc_overrides(analysis, manifest_sources)
    overrides = merge_overrides(source_overrides, file_overrides)

    clean_output(out_dir)
    py_count = generate_python_package_stubs(out_dir, overrides, set(frozen_modules) if frozen_modules else None, manifest_sources)
    c_count = generate_c_module_stubs(out_dir, overrides, analysis)
    resource_lib_count = copy_resource_libs(out_dir)
    write_metadata(out_dir, py_count, c_count, resource_lib_count, override_path, overrides, build_dir, frozen_modules, qstr_sources, analysis)

    print(f"Generated CanMV stubs: {out_dir}")
    print(f"  Python package stubs: {py_count}")
    print(f"  C module stubs: {c_count}")
    print(f"  Resource lib files: {resource_lib_count}")
    print(f"  C modules discovered: {len(analysis.modules)}")
    print(f"  C types discovered: {len(analysis.types)}")
    print(f"  C functions discovered: {len(analysis.functions)}")
    print(f"  Override modules: {len(overrides)}")
    print(f"  Port doc override modules: {len(source_overrides)}")
    if build_dir:
        print(f"  Build inventory: {build_dir}")
        print(f"  Frozen modules: {len(frozen_modules)}")
        print(f"  QSTR sources: {len(qstr_sources)}")
    print(f"  Manifest: {manifest_path}")
    print(f"  Manifest Python sources: {len(manifest_sources)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
